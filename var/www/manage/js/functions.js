function loader(type){
    if(type=='show'){
        $('.modal-footer .btn, .btns .btn').hide();
        if($( ".loader" ).length<1){
            $('.modal-footer, .btns').append('<div class="loader"></div>');
        }
        
    }else{
        $('.modal-footer .btn, .btns .btn').show();
        $('.loader').remove();
    }
}